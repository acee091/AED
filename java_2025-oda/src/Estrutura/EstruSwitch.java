package Estrutura;

public class EstruSwitch {
    public static void main(String[] args) {

        int num = 100;

        switch (num) {

            case 1:
                System.out.println(" Número teste é " + num);
                break;
            case 2:
                System.out.println(" Número teste é " + num);
                break;
            case 3:
                System.out.println(" Número teste é " + num);
                break;
            case 20:
                System.out.println(" Número teste é " + num);
                break;
            case 10:
                System.out.println(" Número teste é " + num);
                break;

                default:
                    System.out.println("A opção " + num + " não foi encontrada");
        }

    }
}
