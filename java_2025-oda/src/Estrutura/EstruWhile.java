package Estrutura;

public class EstruWhile {
    /**
     *   Vamos falar de While
     *    while significa enquanto
     *    logo, enquanto for verdadeira será executada as intrucóes
     *
     */

    public static void main(String[] args) {
        // vamos declarar e iniciar a variável com um valor.

        int num = 0; // podemos começar também com o número ou outro como desejar.

       // agora enquanto este valor for verdadeiro a instrução será executada

        while(num <= 10){

            System.out.println(num);

            num = num + 1;
        }

    }

}
