/**
 *
 *  O for consiste em;
 *  Iniciar a variável com um valor
 *  teste lógico
 *  incrementar x++ = x = x + 1
 *
 *  Para facilitar o uso do Comando
 *  System.out.println(); no Intellig IDEA
 *  Digite sout e aperte a tecla TAB
 *
 */
package Estrutura;

public class EstruFor {
    public static void main(String[] args) {

        for (int x=0;x<=10;x++){
            System.out.println(x);
        }

    }
}
